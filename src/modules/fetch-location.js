var API_KEY; // variable assignment generated by webpack loader
import $ from 'jquery';

export default function(locationId) {
    return $.ajax({
        url: 'http://datapoint.metoffice.gov.uk/public/data/val/wxfcs/all/json/' + locationId,
        data: {
            res: '3hourly',
            key: API_KEY
        },
        method: 'get',
        timeout: 15000
    });
};
